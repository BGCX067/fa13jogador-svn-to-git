package com.fa13.build.model;

public class PlayerTraining {
	public PlayerTraining(int number, int shootingPoints, int passingPoints, int crossPoints, int dribblingPoints, int tacklingPoints, int speedPoints, int staminaPoints, int headingPoints,
			int reflexesPoints, int handlingPoints, int fitnessPoints, int moraleFinance, int fitnessFinance) {
		this.number = number;
		this.shootingPoints = shootingPoints;
		this.passingPoints = passingPoints;
		this.crossPoints = crossPoints;
		this.dribblingPoints = dribblingPoints;
		this.tacklingPoints = tacklingPoints;
		this.speedPoints = speedPoints;
		this.staminaPoints = staminaPoints;
		this.headingPoints = headingPoints;
		this.reflexesPoints = reflexesPoints;
		this.handlingPoints = handlingPoints;
		this.fitnessPoints = fitnessPoints;
		this.moraleFinance = moraleFinance;
		this.fitnessFinance = fitnessFinance;
	}
	private int number;
	private int shootingPoints;
	private int passingPoints;
	private int crossPoints;
	private int dribblingPoints;
	private int tacklingPoints;
	private int speedPoints;
	private int staminaPoints;
	private int headingPoints;
	private int reflexesPoints;
	private int handlingPoints;
	private int fitnessPoints;
	private int moraleFinance;
	private int fitnessFinance;
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public int getShootingPoints() {
		return shootingPoints;
	}
	public void setShootingPoints(int shootingPoints) {
		this.shootingPoints = shootingPoints;
	}
	public int getPassingPoints() {
		return passingPoints;
	}
	public void setPassingPoints(int passingPoints) {
		this.passingPoints = passingPoints;
	}
	public int getCrossPoints() {
		return crossPoints;
	}
	public void setCrossPoints(int crossPoints) {
		this.crossPoints = crossPoints;
	}
	public int getDribblingPoints() {
		return dribblingPoints;
	}
	public void setDribblingPoints(int dribblingPoints) {
		this.dribblingPoints = dribblingPoints;
	}
	public int getTacklingPoints() {
		return tacklingPoints;
	}
	public void setTacklingPoints(int tacklingPoints) {
		this.tacklingPoints = tacklingPoints;
	}
	public int getSpeedPoints() {
		return speedPoints;
	}
	public void setSpeedPoints(int speedPoints) {
		this.speedPoints = speedPoints;
	}
	public int getStaminaPoints() {
		return staminaPoints;
	}
	public void setStaminaPoints(int staminaPoints) {
		this.staminaPoints = staminaPoints;
	}
	public int getHeadingPoints() {
		return headingPoints;
	}
	public void setHeadingPoints(int headingPoints) {
		this.headingPoints = headingPoints;
	}
	public int getReflexesPoints() {
		return reflexesPoints;
	}
	public void setReflexesPoints(int reflexesPoints) {
		this.reflexesPoints = reflexesPoints;
	}
	public int getHandlingPoints() {
		return handlingPoints;
	}
	public void setHandlingPoints(int handlingPoints) {
		this.handlingPoints = handlingPoints;
	}
	public int getFitnessPoints() {
		return fitnessPoints;
	}
	public void setFitnessPoints(int fitnessPoints) {
		this.fitnessPoints = fitnessPoints;
	}
	public int getMoraleFinance() {
		return moraleFinance;
	}
	public void setMoraleFinance(int moraleFinance) {
		this.moraleFinance = moraleFinance;
	}
	public int getFitnessFinance() {
		return fitnessFinance;
	}
	public void setFitnessFinance(int fitnessFinance) {
		this.fitnessFinance = fitnessFinance;
	}
}
